package polafigur2;

/**
 *
 * @author piotr
 */
public class Points {
    int x;
    double y;

    public Points(int x, double y) {
        this.x = x;
        this.y = y;
    }

    public Points() {
    }
    
    

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
